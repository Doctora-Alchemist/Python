"""
Booleanos - Respuestas Verdadero o Falso
Los booleanos son respuestas tipo:
True = Verdadero
False = Falso
"""
# Ejemplo de comparación
mayor_edad = 20 > 18
print(mayor_edad)  # True

# Operadores lógicos
llueve = False
hay_paraguas = True

# Quiero salir si no llueve o si tengo paraguas
salgo = not llueve or hay_paraguas
print(salgo)  # True
